        <script>
        document.addEventListener("DOMContentLoaded", function() {
            const track = document.querySelector('.scroll-gallery-track');
            const items = Array.from(document.querySelectorAll('.scroll-item'));
            if (!track || items.length === 0) return;

            // Duplicate items for seamless scroll
            const totalItems = items.length;
            for (let i = 0; i < totalItems; i++) {
                const clone = items[i].cloneNode(true);
                track.appendChild(clone);
            }

            // Calculate width of track
            let trackWidth = 0;
            const allItems = document.querySelectorAll('.scroll-item');
            allItems.forEach(item => {
                const style = getComputedStyle(item);
                const gap = parseInt(style.marginRight) || 0;
                trackWidth += item.offsetWidth + gap;
            });

            // Set CSS animation dynamically
            const duration = trackWidth / 100; // adjust speed factor (px/sec)
            track.style.animation = `scroll-left ${duration}s linear infinite`;

            // Add keyframes dynamically
            const styleEl = document.createElement('style');
            styleEl.innerHTML = `
                @keyframes scroll-left {
                    0% { transform: translateX(0); }
                    100% { transform: translateX(-${trackWidth/2}px); }
                }
            `;
            document.head.appendChild(styleEl);
        });
